void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft_putchar('-');
		if (nb <= -9)
			ft_putnbr(nb / 10 * -1);
		ft_putchar(nb % 10 * -1 + '0');
	}
	else
	{
		if (nb > 10)
			ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + '0');
	}
}
