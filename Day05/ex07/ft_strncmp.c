int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (*s1 && (*s1 == *s2) && (i < n - 1))
	{
		s1++;
		s2++;
		i++;
	}
	if (*s1 - *s2 > 0)
		return (1);
	else if (*s1 - *s2 < 0)
		return (-1);
	return (0);
}
