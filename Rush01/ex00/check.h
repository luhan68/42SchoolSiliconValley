#ifndef CHECK_H
# define CHECK_H

int		check_board(char *argv[])
{
	int		r;
	int		c;

	r = 0;
	while (r < 9)
	{
		c = 0;
		while (argv[r + 1][c] != '\0')
		{
			if (!((argv[r + 1][c] >= '1' && argv[r + 1][c] <= '9')
						|| argv[r + 1][c] == '.'))
			{
				return (0);
			}
			c++;
		}
		if (c != 9)
			return (0);
		r++;
	}
	return (1);
}

#endif
